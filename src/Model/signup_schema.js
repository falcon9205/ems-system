// models for candidate , trainer & recruiter who can login in our portal
import mongoose from "mongoose";

const candidateSchema = new mongoose.Schema(
  {
    firstname: {
      type: String,
      required: true,
      trim: true,
    },
    lastname: {
      type: String,
      required: true,
      trim: true,
    },

    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const recruiterSchema = new mongoose.Schema(
  {
    firstname: {
      type: String,
      required: true,
      trim: true,
    },
    lastname: {
      type: String,
      required: true,
      trim: true,
    },

    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    contact: {
      type: Number,
      required: true,
    },
    organizationname: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const trainerSchema = new mongoose.Schema(
  {
    firstname: {
      type: String,
      required: true,
      trim: true,
    },
    lastname: {
      type: String,
      required: true,
      trim: true,
    },

    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
    },
    trainerof: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);
const referralSchema = new mongoose.Schema(
  {
    firstname: {
      type: String,
      required: true,
      trim: true,
    },
    lastname: {
      type: String,
      required: true,
      trim: true,
    },

    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);


const Candidate =mongoose.models.candidates || mongoose.model("candidates", candidateSchema);
const Recruiter = mongoose.models.Recruiter || mongoose.model("Recruiter", recruiterSchema);
const Trainer =mongoose.models.Trainer || mongoose.model("Trainer", trainerSchema);
const Referral = mongoose.models.Referral || mongoose.model("Referral",referralSchema)
export { Candidate, Recruiter, Trainer,Referral };
